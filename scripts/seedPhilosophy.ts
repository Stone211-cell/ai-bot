async function main() {
  console.log("Seeding philosophy data...");
  for (const philo of initialPhilosophies) {
    const existing = await prisma.philosophy.findUnique({
      where: { name: philo.name }
    });
    
    if (!existing) {
      await prisma.philosophy.create({
        data: philo
      });
      console.log(`+ Added: ${philo.name}`);
    } else {
      console.log(`= Skipped: ${philo.name} (Already exists)`);
    }
  }
  console.log("Seeding complete.");
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
